fn render(rctx: *RenderContext(MobSuper)) void {
    const ctx = rctx.ctx;
    const entity = rctx.entity;
    const mob = &entity.impl;

    ctx.rotate(entity.angle);

    const scale = entity.size * comptime (1.0 / 70.0);
    ctx.scale(scale, scale);

    ctx.setGlobalAlpha(0.4 * ctx.globalAlpha());

    ctx.copyCanvasWithScale(
        if (mob.is_pet)
            pet
        else
            normal,
        -base_size / 2,
        -base_size / 2,
        base_size,
        base_size,
    );
}

// Radius * fraction
const base_size = 13 * 10;
const resolution = 4;
const size = base_size * resolution;
const half_size = size / 2.0;

fn createWebProjectileCanvas(allocator: mem.Allocator, comptime is_pet: bool) *CanvasContext {
    const ctx = CanvasContext.createCanvasContext(allocator, size, size, false);

    ctx.setImageSmoothingEnabled(true);

    ctx.translate(half_size, half_size);

    ctx.scale(resolution, resolution);

    ctx.fillColor(
        comptime if (is_pet)
            .comptimeFromHex(0xFFE763)
        else
            .comptimeFromHex(0xFFFFFF),
    );
    ctx.fillPath(body, .nonzero);

    return ctx;
}

